public class TipeDataCharacter {
    public static void main(String[] args) {

        // Tipe data character
        // char adalah tipe  data
        //  a adalah variabel
        //  A adalah value/isi dari variabel a dengan tipe data char

        char a = 'A';
        char r = 'R';
        char m = 'M';
        char y = 'Y';

        System.out.println(a);
        System.out.println(r);
        System.out.println(m);
        System.out.println(y);

        System.out.print(a);
        System.out.print(r);
        System.out.print(m);
        System.out.print(y);

    }
}
