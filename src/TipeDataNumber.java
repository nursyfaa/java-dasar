public class TipeDataNumber {
        public static void main(String[] args) {

            //  ============ TIPE DATA NUMBER ===============
            //  Byte adalah tipe data
            //  iniByte adalah nama variabel
            //  100.. adalah value dari variabel iniByte dengan tipe data byte

            // Tipe data Integer Number : Bilangan bulat ada 4: byte, short, int, long
            byte iniByte = 100;
            short iniShort = 1000;
            int intInt = 10000000;
            long intLong = 1000000000;
            long  intLong2 = 100000000L;

            // Floating point number : Bilangan pecahan atau decimal
            float  intFloat = 15.000F;
            double iniDouble = 31.8060;

            //  Literals
            int decimalInt = 99;
            int hexInt = 0x000000;
            int binaryDecimal = 0b100100;

            // Underscore
            // Untuk pemisah agar tidak bingung pemisahnya

            long balance = 1_000_000_000_000L;
            int sum = 60_000_000;
            int amount = 10_000_000;

        }
    }

